#!/usr/bin/env node
/**
 * Generates src/lib/render/template.ts from docs/invoice-template.html.
 *
 * The HTML doc stays the single source of truth; the generated constant makes
 * it importable from both the browser preview and a Node route handler without
 * `fetch` or `fs`. A Vitest drift test fails if the two diverge, so running
 * this script is mandatory after editing the template.
 *
 * Usage: npm run template:sync
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const templatePath = join(repoRoot, "docs", "invoice-template.html");
const outputPath = join(repoRoot, "src", "lib", "render", "template.ts");

const html = readFileSync(templatePath, "utf8");

const banner = `// AUTO-GENERATED by scripts/sync-template.mjs — do not edit by hand.
// Source of truth: docs/invoice-template.html · regenerate: npm run template:sync

/** Raw invoice template markup, byte-identical to docs/invoice-template.html. */
export const INVOICE_TEMPLATE = ${JSON.stringify(html)};
`;

writeFileSync(outputPath, banner, "utf8");
process.stdout.write(`Wrote ${outputPath} (${html.length} chars)\n`);
