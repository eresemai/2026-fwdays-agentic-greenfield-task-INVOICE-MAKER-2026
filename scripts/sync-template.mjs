#!/usr/bin/env node
/**
 * Generates src/lib/render/template.ts from docs/invoice-template.html.
 *
 * Two things happen:
 *
 * 1. The template's single `@import url('https://fonts.googleapis.com/…')` line
 *    is replaced with `@font-face` rules whose `src` is a base64 `data:` URI of
 *    the woff2 files vendored in docs/fonts/. The rendered invoice then has no
 *    external reference at all, so preview, saved HTML, and offline headless
 *    Chromium produce identical documents (FR-TPL-05).
 * 2. The result is emitted as an importable constant, usable from the browser
 *    preview and a Node route handler without `fetch` or `fs`.
 *
 * Every other byte of the template passes through untouched — the TERMS block
 * above all (BC-LEGAL-01).
 *
 * Usage:
 *   npm run template:sync    regenerate after editing the template or fonts
 *   npm run template:check   fail if the generated file is stale (runs in build)
 */
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const templatePath = join(repoRoot, "docs", "invoice-template.html");
const fontsDir = join(repoRoot, "docs", "fonts");
const outputPath = join(repoRoot, "src", "lib", "render", "template.ts");
const checkOnly = process.argv.includes("--check");

/**
 * Inter v20 variable subsets. `unicode-range` values are copied verbatim from
 * Google's CSS so the browser selects faces exactly as it would online.
 * See docs/fonts/README.md for provenance and for why `cyrillic` is mandatory
 * even for English-only invoices (it owns U+2116 NUMERO SIGN).
 */
const FONT_SUBSETS = [
  {
    file: "inter-cyrillic.woff2",
    unicodeRange: "U+0301, U+0400-045F, U+0490-0491, U+04B0-04B1, U+2116",
  },
  {
    file: "inter-latin-ext.woff2",
    unicodeRange:
      "U+0100-02BA, U+02BD-02C5, U+02C7-02CC, U+02CE-02D7, U+02DD-02FF, U+0304, U+0308, U+0329, U+1D00-1DBF, U+1E00-1E9F, U+1EF2-1EFF, U+2020, U+20A0-20AB, U+20AD-20C0, U+2113, U+2C60-2C7F, U+A720-A7FF",
  },
  {
    file: "inter-latin.woff2",
    unicodeRange:
      "U+0000-00FF, U+0131, U+0152-0153, U+02BB-02BC, U+02C6, U+02DA, U+02DC, U+0304, U+0308, U+0329, U+2000-206F, U+20AC, U+2122, U+2191, U+2193, U+2212, U+2215, U+FEFF, U+FFFD",
  },
];

const IMPORT_PATTERN =
  /^[ \t]*@import url\('https:\/\/fonts\.googleapis\.com\/[^']*'\);[ \t]*$/m;
const EXTERNAL_REFERENCE_PATTERN = /https?:\/\//;

function buildFontFaces() {
  return FONT_SUBSETS.map(({ file, unicodeRange }) => {
    const base64 = readFileSync(join(fontsDir, file)).toString("base64");

    return [
      "        /* Inter — embedded so the document needs no network (FR-TPL-05). */",
      "        @font-face {",
      "            font-family: 'Inter';",
      "            font-style: normal;",
      "            font-weight: 300 800;",
      "            font-display: block;",
      `            src: url(data:font/woff2;base64,${base64}) format('woff2');`,
      `            unicode-range: ${unicodeRange};`,
      "        }",
    ].join("\n");
  }).join("\n");
}

function generate() {
  const html = readFileSync(templatePath, "utf8");

  if (!IMPORT_PATTERN.test(html)) {
    throw new Error(
      "docs/invoice-template.html no longer contains the expected Google Fonts @import line; update scripts/sync-template.mjs"
    );
  }

  const embedded = html.replace(IMPORT_PATTERN, buildFontFaces());

  if (EXTERNAL_REFERENCE_PATTERN.test(embedded)) {
    throw new Error(
      "Generated template still contains an external reference; FR-TPL-05 requires none"
    );
  }

  return `// AUTO-GENERATED by scripts/sync-template.mjs — do not edit by hand.
// Source: docs/invoice-template.html + docs/fonts/*.woff2 (inlined as data URIs)
// Regenerate: npm run template:sync

/** Invoice template markup with fonts embedded; no external references. */
export const INVOICE_TEMPLATE = ${JSON.stringify(embedded)};
`;
}

const generated = generate();

if (checkOnly) {
  const current = existsSync(outputPath) ? readFileSync(outputPath, "utf8") : "";

  if (current !== generated) {
    process.stderr.write(
      "src/lib/render/template.ts is stale — run `npm run template:sync`\n"
    );
    process.exit(1);
  }

  process.stdout.write("template.ts is in sync with the template and fonts\n");
} else {
  writeFileSync(outputPath, generated, "utf8");
  process.stdout.write(`Wrote ${outputPath} (${generated.length} chars)\n`);
}
